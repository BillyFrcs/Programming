void main(List<String> arguments) {
  //String to int (can use another data types)
  var number = double.parse('86.5');
  print(number);

  //int to String
  var value = 13.toString();
  print(value);

  //double to String
  var pi = 3.14287.toStringAsFixed(2);
  print(pi);

  var n = int.parse('67');
  print(n);
}
